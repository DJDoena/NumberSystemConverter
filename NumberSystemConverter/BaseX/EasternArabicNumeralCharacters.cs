namespace DoenaSoft.NumberSystemConverter.BaseX;

/// <summary>
/// Characters used in the Eastern Arabic (Arabic-Indic) decimal number system.
/// All characters are used for both input (<see cref="INumberSystemConverter.ToULong"/>) and output (<see cref="INumberSystemConverter.FromULong"/>).
/// Also known as Arabic-Indic numerals, used in Arabic-speaking countries.
/// </summary>
public static class EasternArabicNumeralCharacters
{
    /// <summary>
    /// Digit 0 in Eastern Arabic (٠).
    /// <para>Value: 0</para>
    /// <para>Unicode: U+0660</para>
    /// </summary>
    public const char Zero = '\u0660';

    /// <summary>
    /// Digit 1 in Eastern Arabic (١).
    /// <para>Value: 1</para>
    /// <para>Unicode: U+0661</para>
    /// </summary>
    public const char One = '\u0661';

    /// <summary>
    /// Digit 2 in Eastern Arabic (٢).
    /// <para>Value: 2</para>
    /// <para>Unicode: U+0662</para>
    /// </summary>
    public const char Two = '\u0662';

    /// <summary>
    /// Digit 3 in Eastern Arabic (٣).
    /// <para>Value: 3</para>
    /// <para>Unicode: U+0663</para>
    /// </summary>
    public const char Three = '\u0663';

    /// <summary>
    /// Digit 4 in Eastern Arabic (٤).
    /// <para>Value: 4</para>
    /// <para>Unicode: U+0664</para>
    /// </summary>
    public const char Four = '\u0664';

    /// <summary>
    /// Digit 5 in Eastern Arabic (٥).
    /// <para>Value: 5</para>
    /// <para>Unicode: U+0665</para>
    /// </summary>
    public const char Five = '\u0665';

    /// <summary>
    /// Digit 6 in Eastern Arabic (٦).
    /// <para>Value: 6</para>
    /// <para>Unicode: U+0666</para>
    /// </summary>
    public const char Six = '\u0666';

    /// <summary>
    /// Digit 7 in Eastern Arabic (٧).
    /// <para>Value: 7</para>
    /// <para>Unicode: U+0667</para>
    /// </summary>
    public const char Seven = '\u0667';

    /// <summary>
    /// Digit 8 in Eastern Arabic (٨).
    /// <para>Value: 8</para>
    /// <para>Unicode: U+0668</para>
    /// </summary>
    public const char Eight = '\u0668';

    /// <summary>
    /// Digit 9 in Eastern Arabic (٩).
    /// <para>Value: 9</para>
    /// <para>Unicode: U+0669</para>
    /// </summary>
    public const char Nine = '\u0669';
}
