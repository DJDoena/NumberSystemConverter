    /// Digit 0 in decimal.
    /// <para>Value: 0</para>
    /// <para>Unicode: U+0030</para>
    /// </summary>
    public const char Zero = '0';

    /// <summary>
    /// Digit 1 in decimal.
    /// <para>Value: 1</para>
    /// <para>Unicode: U+0031</para>
    /// </summary>
    public const char One = '1';

    /// <summary>
    /// Digit 2 in decimal.
    /// <para>Value: 2</para>
    /// <para>Unicode: U+0032</para>
    /// </summary>
    public const char Two = '2';

    /// <summary>
    /// Digit 3 in decimal.
    /// <para>Value: 3</para>
    /// <para>Unicode: U+0033</para>
    /// </summary>
    public const char Three = '3';

    /// <summary>
    /// Digit 4 in decimal.
    /// <para>Value: 4</para>
    /// <para>Unicode: U+0034</para>
    /// </summary>
    public const char Four = '4';

    /// <summary>
    /// Digit 5 in decimal.
    /// <para>Value: 5</para>
    /// <para>Unicode: U+0035</para>
    /// </summary>
    public const char Five = '5';

    /// <summary>
    /// Digit 6 in decimal.
    /// <para>Value: 6</para>
    /// <para>Unicode: U+0036</para>
    /// </summary>
    public const char Six = '6';

    /// <summary>
    /// Digit 7 in decimal.
    /// <para>Value: 7</para>
    /// <para>Unicode: U+0037</para>
    /// </summary>
    public const char Seven = '7';

    /// <summary>
    /// Digit 8 in decimal.
    /// <para>Value: 8</para>
    /// <para>Unicode: U+0038</para>
    /// </summary>
    public const char Eight = '8';

    /// <summary>
    /// Digit 9 in decimal.
    /// <para>Value: 9</para>
    /// <para>Unicode: U+0039</para>
    /// </summary>
    public const char Nine = '9';
}
