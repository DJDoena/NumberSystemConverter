namespace DoenaSoft.NumberSystemConverter.BaseX;

/// <summary>
/// Characters used in the Persian (Farsi) decimal number system.
/// All characters are used for both input (<see cref="INumberSystemConverter.ToULong"/>) and output (<see cref="INumberSystemConverter.FromULong"/>).
/// Also known as Eastern Persian numerals, used in Iran and Afghanistan.
/// </summary>
public static class PersianNumeralCharacters
{
    /// <summary>
    /// Digit 0 in Persian (۰).
    /// <para>Value: 0</para>
    /// <para>Unicode: U+06F0</para>
    /// </summary>
    public const char Zero = '\u06F0';

    /// <summary>
    /// Digit 1 in Persian (۱).
    /// <para>Value: 1</para>
    /// <para>Unicode: U+06F1</para>
    /// </summary>
    public const char One = '\u06F1';

    /// <summary>
    /// Digit 2 in Persian (۲).
    /// <para>Value: 2</para>
    /// <para>Unicode: U+06F2</para>
    /// </summary>
    public const char Two = '\u06F2';

    /// <summary>
    /// Digit 3 in Persian (۳).
    /// <para>Value: 3</para>
    /// <para>Unicode: U+06F3</para>
    /// </summary>
    public const char Three = '\u06F3';

    /// <summary>
    /// Digit 4 in Persian (۴).
    /// <para>Value: 4</para>
    /// <para>Unicode: U+06F4</para>
    /// </summary>
    public const char Four = '\u06F4';

    /// <summary>
    /// Digit 5 in Persian (۵).
    /// <para>Value: 5</para>
    /// <para>Unicode: U+06F5</para>
    /// </summary>
    public const char Five = '\u06F5';

    /// <summary>
    /// Digit 6 in Persian (۶).
    /// <para>Value: 6</para>
    /// <para>Unicode: U+06F6</para>
    /// </summary>
    public const char Six = '\u06F6';

    /// <summary>
    /// Digit 7 in Persian (۷).
    /// <para>Value: 7</para>
    /// <para>Unicode: U+06F7</para>
    /// </summary>
    public const char Seven = '\u06F7';

    /// <summary>
    /// Digit 8 in Persian (۸).
    /// <para>Value: 8</para>
    /// <para>Unicode: U+06F8</para>
    /// </summary>
    public const char Eight = '\u06F8';

    /// <summary>
    /// Digit 9 in Persian (۹).
    /// <para>Value: 9</para>
    /// <para>Unicode: U+06F9</para>
    /// </summary>
    public const char Nine = '\u06F9';
}
